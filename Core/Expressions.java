// This class represents the expressive data of brain
package Core;

public class Expressions {
    // Values either 0 or 1
    private int blink;
    private int leftWink;
    private int rightWink;
    private int lookLeft;
    private int lookRight;
    private int raiseBrow;
    private int furrow;

    //Values in the range of 0-1
    private int smile;
    private int leftSmirk;
    private int rightSmirk;
    private int laugh;
    private int clench;
    public int getBlink() {
        return blink;
    }

    public void setBlink(int blink) {
        this.blink = blink;
    }

    public int getLeftWink() {
        return leftWink;
    }

    public void setLeftWink(int leftWink) {
        this.leftWink = leftWink;
    }

    public int getRightWink() {
        return rightWink;
    }

    public void setRightWink(int rightWink) {
        this.rightWink = rightWink;
    }

    public int getLookLeft() {
        return lookLeft;
    }

    public void setLookLeft(int lookLeft) {
        this.lookLeft = lookLeft;
    }

    public int getLookRight() {
        return lookRight;
    }

    public void setLookRight(int lookRight) {
        this.lookRight = lookRight;
    }

    public int getRaiseBrow() {
        return raiseBrow;
    }

    public void setRaiseBrow(int raiseBrow) {
        this.raiseBrow = raiseBrow;
    }

    public int getFurrow() {
        return furrow;
    }

    public void setFurrow(int furrow) {
        this.furrow = furrow;
    }

    public int getSmile() {
        return smile;
    }

    public void setSmile(int smile) {
        this.smile = smile;
    }

    public int getLeftSmirk() {
        return leftSmirk;
    }

    public void setLeftSmirk(int leftSmirk) {
        this.leftSmirk = leftSmirk;
    }

    public int getRightSmirk() {
        return rightSmirk;
    }

    public void setRightSmirk(int rightSmirk) {
        this.rightSmirk = rightSmirk;
    }

    public int getLaugh() {
        return laugh;
    }

    public void setLaugh(int laugh) {
        this.laugh = laugh;
    }

    public int getClench() {
        return clench;
    }

    public void setClench(int clench) {
        this.clench = clench;
    }
}
